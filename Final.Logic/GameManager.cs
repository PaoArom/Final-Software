    public void StartGame()
    {
        var map = new MapManager(20, 20, 1, 1);
        var inventory = new InventoryManager();

        bool playing = true;

        while (playing)
        {
            Console.Clear();
            map.DisplayMap();
            Console.WriteLine();

            if (map.HasKey())
                Console.WriteLine("  ūüóĚÔłŹ  You have the key");
            else
                Console.WriteLine("  ūüĒć Find the key to open the exit");

            Console.WriteLine("  Use WASD to move  E for Inventory  Q for Menu");

            var keyInfo = Console.ReadKey(true);
            string key = keyInfo.Key.ToString().ToUpper();

            if (key == "Q")
            {
                playing = false;
                continue;
            }

            if (key == "E")
            {
                if (map.HasKey() && !inventory.HasItem("ūüóĚÔłŹ  Key"))
                    inventory.PickUpItem("ūüóĚÔłŹ  Key");

                inventory.ShowInventory();
                continue;
            }

            if (key is "W" or "A" or "S" or "D")
            {
                map.MovePlayer(key);

                if (map.DidLose())
                {
                    ShowGameOver();
                    playing = false;
                }
                else if (map.DidWin())
                {
                    ShowLevelCleared();
                    playing = false;
                }
            }
            else if (key != "E" && key != "Q")
            {
                Console.WriteLine("Invalid key Use WASD, E or Q.");
                Console.WriteLine("Press any key to continue...");
                Console.ReadKey(true);
            }
        }
    }
